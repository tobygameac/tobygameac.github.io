function gradeCheck() {
  var grade = [];
  for (var i = 0; i < 5; i++) {
    grade.push(document.getElementById("grade" + i).selectedIndex);
  }
  var result = "通過最低錄取分數科系如下 : <br>";
  for (var i = 0; i < list.length; i++) {
    var detail = list[i].split(" ");
    var sum = 0, full = 0;
    for (var j = 0; j < 5; j++) {
      sum += grade[j] * detail[j + 3];
      full += 15 * detail[j + 3];
    }
    var value = (sum / full) * 100;
    if (value >= detail[8]) {
      var school = detail[1] + " " + detail[2];
      result += school + " +" + (value - detail[8]).toFixed(2) + "<br>";
    }
  }
  document.getElementById("result").innerHTML = result;
}

